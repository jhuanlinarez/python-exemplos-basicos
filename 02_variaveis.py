# variaveis
nome = "Gisele"
idade = 44

#exibilçao de valores
print(nome)
print(idade)

# 1º método de concatenação (string:str)
print("O meu nome é: " + nome + " e tenho " + str(idade) + " anos.")

print ("O meu nome é: {} e tenho {} anos.\n".format(nome,idade))

print(f"O meu nome é: {nome} e tenho {idade} anos.")
print("\n")